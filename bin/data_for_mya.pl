#!/usr/bin/perl -w

# count some data for mya

use strict;
use DBI;
use Bio::SearchIO;

my ($filecount, $querycount, $humanfirst, $hitcount)=(0,0,0,0);
my ($colifirst, $ratfirst, $mousefirst)=(0,0,0);
my %mitochon=&mito();
my %m13=&m13();
my %nohits=&nohits();
my @unknowns;
my %unknown;

my $dbh=DBI->connect("DBI:mysql:ncbi", "rob", "forestry") or die "Can't connect to database\n";
my $dir=shift || die "dir?";
opendir(DIR, $dir) || die "Can't opebn $dir";
while (my $file=readdir(DIR)) {
 next if ($file =~ /^\./);
 $filecount++;
 unless ($filecount % 50) {print STDERR "$filecount files in ", time-$^T, " seconds total\n"}
 next if ($m13{$file} || $nohits{$file} || $mitochon{$file});
 my $sio=Bio::SearchIO->new(-file=>"$dir/$file", -format=>"blast");
 while (my $res=$sio->next_result) {
  my $query=$res->query_name;
  my $len=$res->query_length;
  my $first=1;
  $querycount++;
  my $hit=$res->next_hit;
  unless ($hit) {
   print STDERR "No hit for $file\n";
   $unknown{$file}="No hit?";
   next;
  }
  $hitcount++;
  my $hitname=$hit->name;
  my $hitdesc=$hit->description;
  my $ah = $hitname . " " . $hitdesc;
   
  if ($ah =~ /human/i || $ah =~ /homo/i) {$humanfirst++}
  elsif ($ah =~ /Mus musculus/i || $ah =~ /mouse/) {$mousefirst++}
  elsif ($ah =~ /\brat/i) {$ratfirst++}
  elsif ($ah =~ /coli/) {$colifirst++}
  else {
    $unknown{$file}=$ah;
    # we don't know what the first hit is
    $hitname =~ /\w+\|(\S+?)\|/;
    my $acc=$1;
    # now we have to iterate through what we know and see if it is in the databases. Yuck
    $acc =~ s/\.\d+$//;
    my @res=$dbh->selectrow_array("select GI from GbAccList where genbank = '$acc'");
    my $keep=1;
    if ($res[0]) {
     my $gi=$res[0];
     undef @res;
     @res=$dbh->selectrow_array("select tax_id from gi_taxid_nucl where gi = $gi");
     if ($res[0]) {
      my $tid=$res[0];
      undef @res;
      @res=$dbh->selectrow_array("select division from nodes where tax_id=$tid");
      if ($res[0]) {
       my $div=$res[0];
       undef @res;
       @res=$dbh->selectrow_array("select division_name from division where division_id=$div");
       $unknown{$file}=$res[0];
       print STDERR "\tFound $res[0] for that\n";
      }
     }
    }
   }
 }
}

print "there were $filecount files\nthere were $querycount queries\nthere were ", scalar(keys %nohits), " no hits\n";
print "there were $hitcount hits\n";
print "there were ", scalar(keys %m13), " sequences with m13 in it\n";
print "there were $mousefirst hits with mouse as the first hit\nThere were $ratfirst hits with rat as the first hit.\n";
print "There were $colifirst with coli as the first hit\n$humanfirst hits were human in the TOP hit\n";
print scalar(keys %mitochon), " sequences had mitochondria\n";

my %division;
foreach my $k (keys %unknown) {
 $division{$unknown{$k}}++;
}

print "\n\nThese are the divisions for everything else:\n";
print map {"$_\t$division{$_}\n"} sort {$division{$b} <=> $division{$a}} keys %division;


print STDERR "Couting data took ", time-$^T, " seconds\n";







